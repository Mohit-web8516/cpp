#include <iostream>
#include <string>   // Needed for string type
using namespace std;

int main() {
    // Integer variable
    int age = 21;

    // Floating point variable
    float pi = 3.14;

    // Double variable
    double gravity = 9.81;

    // Character variable
    char grade = 'A';

    // Boolean variable
    bool isStudent = true;

    // String variable
    string name = "Mohit";

    // Output the values
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Pi: " << pi << endl;
    cout << "Gravity: " << gravity << endl;
    cout << "Grade: " << grade << endl;
    cout << "Is Student? " << isStudent << endl;

    return 0;
}
